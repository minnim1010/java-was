    private int insertQuery() throws SQLException {
        Pattern pattern = Pattern.compile("INSERT INTO (\\w+) \\(([^)]+)\\) VALUES \\(([^)]+)\\)");
        Matcher matcher = pattern.matcher(sql.trim().toUpperCase());

        if (!matcher.matches()) {
            throw new SQLException("Invalid INSERT syntax");
        }

        String tableName = matcher.group(1);
        String[] columns = matcher.group(2).split(",");
        String[] values = matcher.group(3).split(",");

        if (columns.length != parameters.size()) {
            throw new SQLException("Mismatch between columns and values count");
        }

        String filePath = connection.getPath() + "/" + tableName + ".csv";
        StringBuilder row = new StringBuilder();

        for (int i = 0; i < parameters.size(); i++) {
            if (i > 0) {
                row.append(",");
            }
            row.append(parameters.get(i).toString());
        }

        CsvUtils.appendCsvFile(filePath, row.toString());
        return 1;
    }

    private int deleteAllQuery() throws SQLException {
        Pattern pattern = Pattern.compile("DELETE FROM (\\w+)");
        Matcher matcher = pattern.matcher(sql.trim().toUpperCase());

        if (!matcher.matches()) {
            throw new SQLException("Invalid DELETE syntax");
        }

        String tableName = matcher.group(1);
        String filePath = connection.getPath() + "/" + tableName + ".csv";
        List<String[]> data = CsvUtils.readCsv(filePath);
        if (data.isEmpty()) {
            return 0;
        }

        List<String[]> newData = new ArrayList<>();
        newData.add(data.get(0));
        CsvUtils.writeCsvFile(filePath, newData);

        return data.size() - 1;
    }

