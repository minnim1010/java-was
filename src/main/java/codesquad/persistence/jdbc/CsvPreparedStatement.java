    @Override
    public ResultSet executeQuery() throws SQLException {
        if (sql.trim().toUpperCase().startsWith("SELECT")) {
            return selectQuery();
        } else {
            throw new SQLException("Unsupported SQL command");
        }
    }

    private ResultSet selectQuery() throws SQLException {
        Pattern pattern = Pattern.compile("SELECT \\* FROM (\\w+)( WHERE (\\w+) = \\?)?", Pattern.CASE_INSENSITIVE);
        Matcher matcher = pattern.matcher(sql.trim());

        if (!matcher.matches()) {
            throw new SQLException("Invalid SELECT syntax");
        }

        String tableName = matcher.group(1);
        String columnName = matcher.group(3);
        String filePath = connection.getPath() + "/" + tableName + ".csv";
        List<String[]> data = CsvUtils.readCsv(filePath);

        List<String[]> result = new ArrayList<>();
        result.add(data.get(0));

        if (columnName == null) {
            result.addAll(data.subList(1, data.size()));
        } else {
            for (int i = 1; i < data.size(); i++) {
                if (data.get(i)[findColumnIndex(data.get(0), columnName)].equals(parameters.get(0))) {
                    result.add(data.get(i));
                }
            }
        }

        return new CsvResultSet(result);
    }

    private int findColumnIndex(String[] headers, String columnName) throws SQLException {
        for (int i = 0; i < headers.length; i++) {
            if (headers[i].equalsIgnoreCase(columnName)) {
                return i;
            }
        }
        throw new SQLException("Column not found: " + columnName);
    }

